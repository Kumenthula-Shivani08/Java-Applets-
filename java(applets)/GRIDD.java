import java.awt.*;
class GRIDD extends Frame
{
	GridLayout g;
	public GRIDD()
	{
		g=new GridLayout(3,3,5,10);
		setLayout(g);
		int i;
		for(i=0;i<9;i++)
		{
			add(new Button("AIML"+(i+1)));
		}
		setVisible(true);
		setSize(500,500);
		setTitle("GRID EXAMPLE");
	}
	public static void main(String [] args)
	{
		new GRIDD();
    }
}